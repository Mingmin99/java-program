package day_0315_chap12_Exam2;

public abstract class TangibleAsset extends Asset {

	public TangibleAsset(String name, String color, int price) {
		super(name, color, price);

	}

	public String getName() {
		return getName();
	}

	public String getColor() {
		return getColor();
	}

	public int getPrice() {
		return getPrice();
	}

}
